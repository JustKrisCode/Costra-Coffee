				<p><span>At Costra Coffee</span>, we have always believed that you need passion to create excellence. Brothers Sergio and Bruno Costra also believed in this.
				<br>
				The story of Costra Coffee begins in 1971, when the brothers Sergio and Bruno arrived in London with a great desire to make coffee with a great taste a part of the everyday life of Londoners. They opened a small bakery on Fenchurch Street with the desire to produce coffee of the highest quality. <br>
				The Costra brothers blind tested 112 different coffee combinations before selecting the one good enough to become our signature blend. They call this blend Mocha Italia and it remains our signature blend to this day. <br>
				<br>
				<span>The perfect blend</span> <br>
				Our iconic Mocha Italia blend is a perfect combination and balance of delicate Arabica and strong Robusta coffee beans that are roasted precisely and pleasantly so that the beans can keep the technique excellent taste and rich aroma. <br>
				<br>
				<span>Development</span> <br>
				In 1978 the brothers open their first Costra Coffee cafe on Vauxhall Bridge Road. <br>
				In this cafeteria, the brothers became the first coffee suppliers in London to offer Espresso and Cappuccino in porcelain cups, thus customers can best enjoy the taste in coffee. <br>
				<br>
				Cafeterias soon began to open in prestigious London locations, becoming destinations for trendy Londoners.
				<br>
				In 1988, due to an increase in demand for their coffee, the brothers decided to move to South London where they converted a huge site into a brand new bakery.  <br>
				The new site is located on Old Paradise Street, Lambeth, where the Costra Coffee bakery remains until 2017.
				<br>
				<br>
				<span>The new bakery</span> <br>
				After more than forty years, Costra Coffee continues to expand in London, the UK and globally around the world. This creates the need to find a new bakery to meet new demand. <br>
				At the beginning of 2017 is opening a new Costra Coffee bakery in Basildon, Essex. The roastery is one of the largest in Europe and enables the production of 45,000 tonnes of coffee per year, quadrupling the roasting capacity of the previous roastery in Lambeth.
				<br>
				<br>
				<span>Costra Coffee in Bulgaria</span> <br>
				The first Costra Coffee cafeteria in Bulgaria opened its doors in Sofia in 2006. The establishments of the English brand in our country are operated by "Coffee Life" EOOD. The company is a franchise partner of Costra Coffee in the country. Today "Kofi Life" EOOD manages a chain of 23 cafeterias in the cities of Sofia, Varna, Plovdiv, Burgas and other cities.</p>

